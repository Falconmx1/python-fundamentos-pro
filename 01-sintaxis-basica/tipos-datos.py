# --- TIPOS DE DATOS BÁSICOS ---

# 1. Números: int (enteros) y float (decimales)
entero = 42
flotante = 3.1416
print("Entero:", entero, "| Flotante:", flotante)

# 2. Cadenas de texto (strings)
texto = "Hola, mundo"
texto_comillas_simples = 'Python es genial'
texto_multilinea = """Este es un texto
que ocupa varias líneas."""
print("String:", texto)
print("String multilínea:", texto_multilinea)

# 3. Booleanos
verdadero = True
falso = False
print("Verdadero:", verdadero, "| Falso:", falso)

# 4. Listas (colección ordenada y mutable)
mi_lista = [1, 2, 3, "cuatro", 5.0]
print("Lista:", mi_lista)

# 5. Tuplas (colección ordenada e inmutable)
mi_tupla = (10, 20, 30)
print("Tupla:", mi_tupla)

# 6. Diccionarios (clave-valor)
mi_diccionario = {"nombre": "Carlos", "edad": 25}
print("Diccionario:", mi_diccionario)

# 7. Conjuntos (colección no ordenada sin duplicados)
mi_conjunto = {1, 2, 3, 3, 2}  # Los duplicados se eliminan
print("Conjunto:", mi_conjunto)

# 8. None (representa la ausencia de valor)
nada = None
print("None:", nada)
