# --- VARIABLES EN PYTHON ---
# Python es de tipado dinámico: no necesitas declarar el tipo de dato.

# 1. Asignación básica
nombre = "Ana"          # String (cadena de texto)
edad = 30               # Integer (entero)
altura = 1.65           # Float (decimal)
es_estudiante = True    # Boolean (verdadero/falso)

# 2. Múltiples variables en una línea
a, b, c = 5, 10, 15

# 3. Reglas para nombrar variables:
# - Deben comenzar con una letra o guión bajo (_).
# - Pueden contener letras, números y guiones bajos.
# - Son sensibles a mayúsculas (nombre != Nombre).

# 4. Imprimir variables
print("Nombre:", nombre)
print("Edad:", edad)
print("Altura:", altura)
print("¿Es estudiante?:", es_estudiante)
print("Valores a, b, c:", a, b, c)
